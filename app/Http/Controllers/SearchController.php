     */
    public function store(Request $request)
    {
        
        $this->validate($request, [
            'keyword' => 'required'
        ]);

        $datesearch = $request->input('keyword');
    
        if($datesearch != null){
            $sales = Sale::where('product_name', '=', $datesearch)
                        ->orWhere('sales_date', '=', $datesearch)
                        ->orWhere('customer_name', '=', $datesearch)->get();
        return view('sales.searchresult')->with('sales', $sales);
